def calcular_pagamento(horas, valor_hora):
    horas = int(horas)
    valor_hora = float(valor_hora)
    salario = horas * valor_hora
    print(salario)
calcular_pagamento(48, 12.5)
calcular_pagamento(12, 12.5)
calcular_pagamento(98, 12.5)
calcular_pagamento(120, 12.5)
